<script>
    const prevButtonEvents = document.getElementById('prevButton-events');
    const nextButtonEvents = document.getElementById('nextButton-events');
    const carouselContainerEvents = document.getElementById('carousel-container-events');

    // Width of a single card plus the gap
    const cardWidthEvents = carouselContainerEvents.querySelector('.card').offsetWidth + 20;
    let currentIndexEvents = 0;

    // Handle next button click for events carousel
    nextButtonEvents.addEventListener('click', () => {
        const maxScrollEvents = carouselContainerEvents.scrollWidth - carouselContainerEvents.clientWidth;
        if (currentIndexEvents < maxScrollEvents) {
            currentIndexEvents += cardWidthEvents;
            carouselContainerEvents.scrollTo({
                left: currentIndexEvents,
                behavior: 'smooth'
            });
        }
    });

    // Handle previous button click for events carousel
    prevButtonEvents.addEventListener('click', () => {
        if (currentIndexEvents > 0) {
            currentIndexEvents -= cardWidthEvents;
            carouselContainerEvents.scrollTo({
                left: currentIndexEvents,
                behavior: 'smooth'
            });
        }
    });
</script>

